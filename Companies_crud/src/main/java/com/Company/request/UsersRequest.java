package com.Company.request;

public class UsersRequest {

	Long id;
	Long companyId;
	String firstName;
	String lastName;
	String type;
	String email;
	
	
	public Long getId() {
		return id;
	}
	
	public Long getCompanyId() {
		return companyId;
	}
	
	public String getFirstName() {
		return firstName;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public String getType() {
		return type;
	}
	
	public String getEmail() {
		return email;
	}
	
	
	
	
}
