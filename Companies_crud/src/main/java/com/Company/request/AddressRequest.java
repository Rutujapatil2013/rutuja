package com.Company.request;

public class AddressRequest {
	
	Long id;
	Long companyId;
	String city;
	String state;
	String country;
	String pincode;
	
	public Long getId() {
		return id;
	}
	
	public Long getCompanyId() {
		return companyId;
	}
	
	public String getCity() {
		return city;
	}
	
	public String getState() {
		return state;
	}
	
	public String getCountry() {
		return country;
	}
	
	public String getPincode() {
		return pincode;
	}
	
	
	
	
}
